function displaylistclubs(listclubs) {



let body = document.getElementById("list-clubs");
 let s = '';
 for (let i = 0; i < listclubs.length; i++) {
     const clubs = listclubs[i];
     let tr = 
     `<div class="col md-4"><div class="card"> 
     <img class="card-img-top" height ="240px" src="${clubs.Image}" alt="Card image">
     <div class="card-body" >
         <h4 >${clubs.Name}</h4>
         <p >${clubs.Position}</p>
         <p >${clubs.Club}</p>
         <a  href="${clubs.Link}" target="blank" class="btn btn-primary"> View
                        Profile</a>
     </div>
     </div> 
     </div>`;
     s += tr;


 }


 body.innerHTML = s;

}
